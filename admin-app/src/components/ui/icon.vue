const cache = {};

function buildSvg(name) {
  if (cache[name]) return cache[name];
  const path = ICON_PATHS[name];
  if (!path) return '';
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">${path}</svg>`;
  // #ifdef H5
  cache[name] = `data:image/svg+xml;utf8,${encodeURIComponent(svg)}`;
  // #endif
  // #ifndef H5
  cache[name] = `data:image/svg+xml;base64,${uni.arrayBufferToBase64(stringToUint8Array(svg))}`;
  // #endif
  return cache[name];
}

function stringToUint8Array(str) {
  const arr = [];
  for (let i = 0; i < str.length; i++) {
    const c = str.charCodeAt(i);
    if (c < 0x80) arr.push(c);
    else if (c < 0x800) {
      arr.push(0xc0 | (c >> 6));
      arr.push(0x80 | (c & 0x3f));
    } else {
      arr.push(0xe0 | (c >> 12));
      arr.push(0x80 | ((c >> 6) & 0x3f));
      arr.push(0x80 | (c & 0x3f));
    }
  }
  return new Uint8Array(arr);
}

