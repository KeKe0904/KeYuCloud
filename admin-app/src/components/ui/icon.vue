  name: 'Icon',
  props: {
    name: { type: String, required: true },
    size: { type: [Number, String], default: 40 }, // rpx
    color: { type: String, default: '' },
  },
  emits: ['click'],
  computed: {
    dataUrl() {
      return buildSvg(this.name);
    },
    realSize() {
      const s = typeof this.size === 'number' ? this.size + 'rpx' : this.size;
      return s;
    },
    sizeClass() {
      const s = typeof this.size === 'number' ? this.size : parseInt(this.size, 10);
      if (s <= 32) return 'ic-xs';
      if (s <= 40) return 'ic-sm';
      if (s <= 48) return '';
      return 'ic-lg';
    },
    iconStyle() {
      const style = {};
      if (this.color) style.color = this.color;
      return style;
    },
  },
};
